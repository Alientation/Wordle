	public PixelFont() {
		super("res\\fonts\\pixelfont_normal.png", "res\\fonts\\pixelfont_bold.png", "res\\fonts\\pixelfont_italic.png", numFonts);
		for (int x = 0; x < numFonts; x++) {
			hm.put(characters[x], x);
		}
		loadImages(bi);
	}
	
	public BufferedImage getBufferedImage(char c, int style) { return bis[style][hm.get(c)]; }
	
